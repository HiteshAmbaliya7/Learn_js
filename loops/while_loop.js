// the simple structure of while loop
// while loop is also entry control loop. first check condition then enter the loop.
// while loop only take condition as a parameter.
let n = 0;

while (n <= 3) {
  console.log(n);
  n++;
}
console.log("out of loop");
const arr = [1, 2, 3, 4, 5, 6, 7, 8, 9];
let i = 0;
while (i < arr.length) {
  console.log(arr[i]);
  i++;
}
console.log("out of loop");
